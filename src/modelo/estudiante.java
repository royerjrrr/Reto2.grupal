package modelo;


public class estudiante {
    private String nombre, materia, genero;
    private int id;

    public estudiante() {
    }
    private double nota;

    public estudiante(String nombre, String materia, String genero, int id, double nota) {
        this.nombre = nombre;
        this.materia = materia;
        this.genero = genero;
        this.id = id;
        this.nota = nota;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getMateria() {
        return materia;
    }

    public void setMateria(String materia) {
        this.materia = materia;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getNota() {
        return nota;
    }

    public void setNota(double nota) {
        this.nota = nota;
    }
    
    
}
